package com.globalsolution.wellbeing_api.domain.model.enums;

/**
 * Representa o nível de severidade de uma condição emocional.
 */
public enum Severidade {
    LEVE,       // Sintomas leves que podem ser gerenciados com intervenções simples
    MODERADA,   // Sintomas moderados que requerem atenção e possível intervenção
    GRAVE       // Sintomas graves que necessitam de intervenção imediata
}
